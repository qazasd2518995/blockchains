export const zhHant: Dict = convertDict(zhHans) as Dict;

function convertDict(value: unknown): unknown {
  if (typeof value === 'string') return toTraditional(value);
  if (Array.isArray(value)) return value.map(convertDict);
  if (value && typeof value === 'object') {
    return Object.fromEntries(
      Object.entries(value as Record<string, unknown>).map(([key, entry]) => [
        key,
        convertDict(entry),
      ]),
    );
  }
  return value;
}

export function toTraditional(value: string): string {
  return Object.entries(SIMPLIFIED_TO_TRADITIONAL)
    .sort((a, b) => b[0].length - a[0].length)
    .reduce((text, [simplified, traditional]) => text.split(simplified).join(traditional), value);
}

export function toSimplified(value: string): string {
  const overrides: Record<string, string> = {
    餘: '余',
    註: '注',
  };
  const invertedEntries = Object.entries(SIMPLIFIED_TO_TRADITIONAL)
    .map(([simplified, traditional]) => [traditional, simplified] as const)
    .sort((a, b) => b[0].length - a[0].length);
  return invertedEntries
    .reduce((text, [traditional, simplified]) => text.split(traditional).join(simplified), value)
    .replace(/[餘註]/g, (char) => overrides[char] ?? char);
}
