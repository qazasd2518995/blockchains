export const zhHans: Dict = simplifyDict(zhHant) as Dict;

function simplifyDict(value: unknown): unknown {
  if (typeof value === 'string') return toSimplified(value);
  if (Array.isArray(value)) return value.map(simplifyDict);
  if (value && typeof value === 'object') {
    return Object.fromEntries(
      Object.entries(value as Record<string, unknown>).map(([key, entry]) => [
        key,
        simplifyDict(entry),
      ]),
    );
  }
  return value;
}

function toSimplified(value: string): string {
  return Object.entries(TRADITIONAL_TO_SIMPLIFIED).reduce(
    (text, [traditional, simplified]) => text.split(traditional).join(simplified),
    value,
  );
}
